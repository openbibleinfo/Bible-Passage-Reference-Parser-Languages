describe("Localized book Rev (heh)", () => {
	let p = {}
	beforeEach(() => {
		p = new bcv_parser(lang);
		p.set_options({ book_alone_strategy: "ignore", book_sequence_strategy: "ignore", osis_compaction_strategy: "bc", captive_end_digits_strategy: "delete", testaments: "ona" });
	});
	it("should handle book: Rev (heh)", () => {
		expect(p.parse("Uwugubulilwe wa Yohane 1:1").osis()).toEqual("Rev.1.1");
		expect(p.parse("Uwu 1:1").osis()).toEqual("Rev.1.1");
	});
});
