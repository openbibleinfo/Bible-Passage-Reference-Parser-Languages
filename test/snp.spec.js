describe("Localized book Rev (snp)", () => {
	let p = {}
	beforeEach(() => {
		p = new bcv_parser(lang);
		p.set_options({ book_alone_strategy: "ignore", book_sequence_strategy: "ignore", osis_compaction_strategy: "bc", captive_end_digits_strategy: "delete", testaments: "ona" });
	});
	it("should handle book: Rev (snp)", () => {
		expect(p.parse("Oloto Piyami Ka 1:1").osis()).toEqual("Rev.1.1");
		expect(p.parse("Oloto Pibo Ha 1:1").osis()).toEqual("Rev.1.1");
		expect(p.parse("Oph 1:1").osis()).toEqual("Rev.1.1");
		expect(p.parse("Opk 1:1").osis()).toEqual("Rev.1.1");
	});
});
